package org.example.educambiental.dto;

import jakarta.validation.constraints.Email;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.Size;
import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.Data;
import lombok.NoArgsConstructor;

/**
 * Petición para que un ADMIN_SYSTEM cree una cuenta administrativa
 * (ADMIN_CONTENT o ADMIN_SYSTEM) desde el panel web.
 */
@Data
@NoArgsConstructor
@AllArgsConstructor
@Builder
public class CrearAdminRequestDto {

    @NotBlank(message = "El nombre es obligatorio")
    private String nombre;

    @NotBlank(message = "El correo es obligatorio")
    @Email(message = "El correo debe tener un formato válido")
    private String correo;

    @NotBlank(message = "La contraseña es obligatoria")
    @Size(min = 6, message = "La contraseña debe tener al menos 6 caracteres")
    private String password;

    @NotBlank(message = "El rol es obligatorio")
    private String rol; // ADMIN_SYSTEM | ADMIN_CONTENT
}
